int		ft_is_prime(int nb)
{
	int n;

	n = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (nb % n != 0)
		n++;
	if (nb == n)
		return (1);
	return (0);
}
